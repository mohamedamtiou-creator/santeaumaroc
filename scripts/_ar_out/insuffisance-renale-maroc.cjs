<p>تصفّي الكليتان الدم، وتزيلان الفضلات وفائض الماء في البول، وتنظّمان الضغط والمعادن، وتصنعان مواد مفيدة (منها تلك التي تحفّز كريات الدم الحمراء). <strong>القصور الكلوي</strong> هو تراجع هذه القدرة على التصفية.</p>
